package org.ninehertzindia.clipped.genrics;

public class AssignedStatus {
	
	private String status;
	private String msg;
	private AssignedData data;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public AssignedData getData() {
		return data;
	}

	public void setData(AssignedData data) {
		this.data = data;
	}

}
